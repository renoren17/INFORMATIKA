# Variabel adalah tempat untuk menyimpan data

# Contoh variabel
nama = "Cinta"
umur = 16
tinggi = 160.5
hobi = "Diecast"

# Cetak isi variabel
print("Nama:", nama)
print("Umur:", umur)
print("Tinggi:", tinggi)
print("Hobi:", hobi)

# Variabel bisa digabung
print("Halo, nama saya " + nama + ". Saya suka " + hobi + ".")

# Mengubah isi variabel
umur = 17
print("Umur sekarang:", umur)
